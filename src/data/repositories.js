// Helper Functions
export const getFeaturedRepos = () => repositories.filter(repo => repo.featured);

export const getReposByTechnology = (tech) => {
  return repositories.filter(repo => 
    repo.technologies.some(t => t.toLowerCase().includes(tech.toLowerCase()))
  );
};

export const searchRepos = (query) => {
  const lowerQuery = query.toLowerCase();
  return repositories.filter(repo => 
    repo.name.toLowerCase().includes(lowerQuery) ||
    repo.description.toLowerCase().includes(lowerQuery) ||
    repo.topics.some(topic => topic.toLowerCase().includes(lowerQuery))
  );
};

export const getAllTechnologies = () => {
  const techSet = new Set();
  repositories.forEach(repo => {
    repo.technologies.forEach(tech => techSet.add(tech));
  });
  return Array.from(techSet).sort();
};
